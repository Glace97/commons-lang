import org.junit.jupiter.api.*;

public class DiffBuilderTest {

    @Test
    void testGetLeft() {
        Integer left = 5;
        DiffBuilder<Integer> builder = new DiffBuilder.Builder<Integer>()
                .setLeft(left)
                .setRight(10)
                .build();
        assertEquals(left, builder.getLeft());
    }

    @Test
    void testGetRight() {
        Integer right = 10;
        DiffBuilder<Integer> builder = new DiffBuilder.Builder<Integer>()
                .setLeft(5)
                .setRight(right)
                .build();
        assertEquals(right, builder.getRight());
    }

}